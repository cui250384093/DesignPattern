public class CommandPattern {
    /**
     * 命令模式（Command Pattern） ：在软件设计中，经常需要向某些对象发送请求，但是并不知道请求的接受者是谁，也不知道请求的操作是哪个，只需要在程序运行时制定具体的请求接受者即可。
     * 命令模式使得请求发送者与请求接受者消除彼此之间的耦合，让对象之间的调用关系更加灵活，实现解耦。
     * 命令模式中，会将一个请求封装为一个对象，一边使用不同的参数来表示不同的请求，同时命令模式也支持可撤销的操作。
     */
}
